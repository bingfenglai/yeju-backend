package pers.lbf.yeju.gateway.web.handler;

import org.springframework.boot.autoconfigure.web.ErrorProperties;
import org.springframework.boot.autoconfigure.web.ResourceProperties;
import org.springframework.boot.autoconfigure.web.reactive.error.DefaultErrorWebExceptionHandler;
import org.springframework.boot.web.reactive.error.ErrorAttributes;
import org.springframework.context.ApplicationContext;
import org.springframework.http.HttpStatus;
import org.springframework.http.MediaType;
import org.springframework.web.reactive.function.server.RouterFunction;
import org.springframework.web.reactive.function.server.ServerRequest;
import org.springframework.web.reactive.function.server.ServerResponse;
import reactor.core.publisher.Mono;

import java.util.Map;

/**
 * @author 赖柄沣 bingfengdev@aliyun.com
 * @version 1.0
 * @Description TODO
 * @date 2020/12/20 15:33
 */
@Deprecated
public class CurrentErrorWebExceptionHandler extends DefaultErrorWebExceptionHandler {

    public CurrentErrorWebExceptionHandler(ErrorAttributes errorAttributes, ResourceProperties resourceProperties, ErrorProperties errorProperties, ApplicationContext applicationContext) {
        super(errorAttributes, resourceProperties, errorProperties, applicationContext);
    }

    @Override
    protected RouterFunction<ServerResponse> getRoutingFunction(ErrorAttributes errorAttributes) {
        return super.getRoutingFunction(errorAttributes);
    }

    @Override
    protected Mono<ServerResponse> renderErrorView(ServerRequest request) {
        return super.renderErrorView(request);
    }

    @Override
    protected Mono<ServerResponse> renderErrorResponse(ServerRequest request) {

        return super.renderErrorResponse(request);
    }

    @Override
    protected boolean isIncludeStackTrace(ServerRequest request, MediaType produces) {
        return super.isIncludeStackTrace(request, produces);
    }

    @Override
    protected int getHttpStatus(Map<String, Object> errorAttributes) {

        return HttpStatus.OK.value();
    }


}
