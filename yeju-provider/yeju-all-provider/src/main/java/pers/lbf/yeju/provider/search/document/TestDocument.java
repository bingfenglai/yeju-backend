package pers.lbf.yeju.provider.search.document;

import org.springframework.data.annotation.Id;
import org.springframework.data.elasticsearch.annotations.Document;
import org.springframework.data.elasticsearch.annotations.Field;
import org.springframework.data.elasticsearch.annotations.FieldType;

import java.io.Serializable;

/**
 * TODO
 *
 * @author 赖柄沣 bingfengdev@aliyun.com
 * @version 1.0
 * @date 2021/4/26 21:20
 */
@Document(indexName = "test", type = "_doc", shards = 1, replicas = 0)
public class TestDocument implements Serializable {

    @Id
    private Long id;

    @Field(type = FieldType.Keyword)
    private String name;

    public TestDocument(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public TestDocument() {

    }

    @Override
    public String toString() {
        return "TestDocument{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
