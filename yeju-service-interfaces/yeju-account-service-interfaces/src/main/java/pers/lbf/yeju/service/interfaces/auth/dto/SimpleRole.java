package pers.lbf.yeju.service.interfaces.auth.dto;

import java.io.Serializable;
import java.util.Date;

/**
 * TODO
 *
 * @author 赖柄沣 bingfengdev@aliyun.com
 * @version 1.0
 * @date 2021/2/16 10:34
 */
public class SimpleRole implements Serializable {

    private Long roleId;
    /** 角色名称 */
    private String roleName;
    /** 角色字符串 */
    private String roleCode;
    /** 角色状态索引 */
    private Long roleStatus;


    private String roleStatusValueStr;
    /** 创建时间 */
    private Date createTime;

    public Long getRoleId() {
        return roleId;
    }

    public void setRoleId(Long roleId) {
        this.roleId = roleId;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public String getRoleCode() {
        return roleCode;
    }

    public void setRoleCode(String roleCode) {
        this.roleCode = roleCode;
    }

    public Long getRoleStatus() {
        return roleStatus;
    }

    public void setRoleStatus(Long roleStatus) {
        this.roleStatus = roleStatus;
    }

    public String getRoleStatusValueStr() {
        return roleStatusValueStr;
    }

    public void setRoleStatusValueStr(String roleStatusValueStr) {
        this.roleStatusValueStr = roleStatusValueStr;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}
