public class SimplePostInfoBean implements Serializable {
    //岗位名称
    private String postName;

    //岗位编码
    private String postCode;
    //备注
    private String remark;
    //岗位状态0未启用1启用
    private String postStatus;

    private String postStatusStr;

    //创建时间
    private Date createTime;

    public String getPostStatusStr() {
        return postStatusStr;
    }

    public void setPostStatusStr(String postStatusStr) {
        this.postStatusStr = postStatusStr;
    }

    public String getPostName() {
        return postName;
    }

    public void setPostName(String postName) {
        this.postName = postName;
    }

    public String getPostCode() {
        return postCode;
    }

    public void setPostCode(String postCode) {
        this.postCode = postCode;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getPostStatus() {
        return postStatus;
    }

    public void setPostStatus(String postStatus) {
        this.postStatus = postStatus;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}
